// Helper function to generate nodes
const generateNodes = (
  storyId: string,
  count: number,
  titles: string[],
  narrations: string[],
  images: string[],
  endingCount: number,
) => {
  return Array.from({ length: count }, (_, i) => ({
    id: `${storyId}-${i + 1}`,
    storyId,
    type: (i === 0
      ? 'start'
      : i >= count - endingCount
      ? 'ending'
      : 'decision') as 'start' | 'decision' | 'ending',
    title: titles[i] || `Scene ${i + 1}`,
    imageUrl: images[i % images.length],
    narration: narrations[i] || `Continue your journey through the story.`,
    choices:
      i >= count - endingCount
        ? []
        : [
            {
              id: `${storyId}-c${i * 2 + 1}`,
              text: `Choice A`,
              targetNodeId: `${storyId}-${i + 2}`,
              order: 1,
            },
            {
              id: `${storyId}-c${i * 2 + 2}`,
              text: `Choice B`,
              targetNodeId: `${storyId}-${Math.min(i + 3, count)}`,
              order: 2,
            },
          ],
  }));
};

